<h1 class="subhead">Intersection of Sets</h1>
<div class="container">
	<div class="flexitem">
		<p>If two sets A and B are given, then the intersection of A and B is the set which consist of elements common to both A and B. It is denoted by the symbol <b class="highlight">∩</b>.This operation can be represented as<br><i class="highlight">A <b style="font-style: normal;">∩</b> B = {x : x ∈ A and x ∈ B}</i><br>Where x is the elements present in both the sets A and B.</p>
		<p>Eg: If set A = {1,2,3,4,5} and B = {4,5,6,7}

Then, Intersection of sets, A ∩ B = {4,5}</p>
