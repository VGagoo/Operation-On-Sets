<h1 class="subhead">Union of Sets</h1>
<div class="container">
	<div class="flexitem">
		<p>If two sets A and B are given, then the union of A and B is equal to the set that contains all the elements, present in set A and set B. It is represented using <b class="highlight">U</b>. This operation can be represented as<br><i class="highlight">
		A <b style="font-style: normal;">U</b> B = {x: x ∈ A or x ∈ B}</i><br>Where x is the elements present in both the sets A or B.</p>
		<p>Eg: If set A = {1,2,3,4} and B = {4,5,6,7}

Then, Union of sets, A ∪ B = {1,2,3,4,5,6,7}</p>
